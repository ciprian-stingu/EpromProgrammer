#include "src/QtGui.h"
#include <stdlib.h>
#include <sys/resource.h>
//------------------------------------------------------------------------------

int main(int argc, char **argv) 
{
    struct rlimit core_limits;
    core_limits.rlim_cur = core_limits.rlim_max = RLIM_INFINITY;
    setrlimit(RLIMIT_CORE, &core_limits);

    QtGui *gui = new QtGui(argc, argv); 
    gui->Run();
    
    delete gui;
	return 0;
}
